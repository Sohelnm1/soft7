export const SourceIcon = ({ className = '', size = 24, color = 'currentColor', source }: IconProps & { source?: string }) => {
  const normalizedSource = source?.toLowerCase() || '';
  
  switch (normalizedSource) {
    case 'instagram':
      return <InstagramIcon className={className} size={size} color={color} />;
    case 'facebook':
      return <FacebookIcon className={className} size={size} color={color} />;
    case 'whatsapp':
      return <MessageCircleIcon className={className} size={size} color={color} />;
    case 'website':
      return <GlobeIcon className={className} size={size} color={color} />;
    default:
      return <DatabaseIcon className={className} size={size} color={color} />;
  }
};
